            <h2>Services</h2>
            <h3>Information/Referral and Follow-Along</h3> 
            <p>Information is provided and referrals are made for services appropriate to individuals who contact the agency with needs and/or questions. Follow-up services are also provided to ensure that individuals successfully locate available programs and services in the community.</p>

            <h3>Equipment Loans & Grants</h3>
            <p>Temporary and/or permanent loans of durable medical equipment, such as wheelchairs, feeding seats, prone standers, strollers, adapted toys,  bath seats, etc., are provided without charge regardless of disability.</p>

            <h3>Computer Tech Lab: (Tulsa)</h3>
            <p>The lab features computers with adaptive input devices for assessment, training and/or personal computer time enabling individuals with disabilities to improve skills, cognizance and confidence through technology.</p>

            <h3>Special Friends</h3>
            <p>A bowling league for children, adolescents and adults with disabilities. Meets Tuesday evenings at AMF Moore Lanes.</p>

            <h3>New Voices</h3>
            <p>Thanks to the latest digital technology in the form of the Apple iPad tablet computer, children who are non-verbal are learning to &ldquo;speak&rdquo; for the first time and communicate with family, friends, and teachers. The iPad's unique size and portability makes it a good fit for children with disabilities. The iPads come with comprehensive language software and numerous visual and audio applications, or &ldquo;apps.&rdquo;</p>

            <p>The iPad is available fo    r those who qualify through an Assistive Technology grant from Ability Connection Oklahoma, made possible by the generous support of the Circle K Corporation and their 55 Central Oklahoma stores.</p>
            
            <h3>Directors Fund</h3>
            <p>As funding permits, Ability Connection Oklahoma  can provide assistance in funding for specific expenses related to disability that have no other source of funding. Through a simple application process, a committee reviews requests in the following areas; small specialized adaptive aids, diapers, specialized infant formulas, critical medications, emergency rental costs and emergency respite.</p>

            <h3>Moms' and Dads' Support Group</h3>
            <p>A collaborative effort with the Tulsa ARC. The purpose of this support group is to provide a nurturing and supportive forum for parents and grandparents of children with disabilities. (Tulsa)</p>

            <h3>Our Special Children</h3>
            <p>(Tulsa) Support and education for Tulsa area Hispanic families with children with disabilities.</p>

            <h3>Families in Crisis</h3>
            <p>Assistance for families facing imminent emergency or crisis, through a collaboration with other agencies. Ability Connection Oklahoma  staff assists and advocates for families who become overwhelmed by the responsibility of caring for a family member with multiple disabilities.</p>

            <h3>Northcliff Gardens</h3>
            <p>(Norman) Sponsored by Ability Connection Oklahoma in collaboration with the Norman Housing authority. This HUD Section 202 Housing complex consists of 44 living units for low income elderly.</p>

            <h3>The Willows/Pathways</h3>
            <p>(Norman) Ability Connection Oklahoma sponsored HUD 202 accessible living accommodations for adults with disabilities. Program consists of 44 units across 4 locations. </p>

